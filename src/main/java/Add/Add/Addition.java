package Add.Add;

public class Addition 
{
	public static void main(String[] args)
	{
		double x,y;
		x = 5.6;
		y = 4.4;
		
		double ans;
		
		ans = x+y;
		
		System.out.println("Answer of " + x + " + "+y+" is = " + ans);
		
		double subs = x - y;
		
		System.out.println("Answer of " + x + " - "+y+" is = " + subs);
	}

}
